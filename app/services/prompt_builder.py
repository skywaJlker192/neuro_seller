from app.niche.loader import load_niche
import yaml
from pathlib import Path


class PromptBuilder:
    """Строит универсальные промпты для любой ниши"""

    def __init__(self, niche_config):
        self.niche = niche_config
        self.catalog = self._load_catalog()

    def _load_catalog(self) -> dict:
        """Загружает каталог (товары или услуги)"""
        try:
            niche_file = Path("niches/default.yaml")
            if niche_file.exists():
                with open(niche_file, "r", encoding="utf-8") as f:
                    data = yaml.safe_load(f)
                    return data.get("product_catalog", {})
        except Exception as e:
            print(f"Error loading catalog: {e}")
        return {}

    def build_system_prompt(self) -> str:
        """Строит УНИВЕРСАЛЬНЫЙ системный промпт"""

        catalog_text = self._format_catalog()

        prompt = f"""Ты — виртуальный консультант компании "{self.niche.business_name}".

## О КОМПАНИИ:
{self.niche.product_description}

## КАТАЛОГ (товары/услуги/цены):
{catalog_text if catalog_text else "Информация о каталоге будет предоставлена в процессе диалога"}

## СТРОГИЕ ПРАВИЛА:

### 1. ЗАПРЕЩЕНО:
- ЗДОРОВАТЬСЯ в ответах (никогда не пиши "здравствуйте", "привет", "добрый день")
- Использовать эмодзи и смайлики
- Писать "пожалуйста, сообщите", "если нужна информация, уточните"
- Задавать лишние вопросы

### 2. ОБЯЗАТЕЛЬНО:
- Отвечать КРАТКО (2-4 предложения)
- ДАВАТЬ конкретику сразу
- Указывать цены
- НЕ здороваться

### 3. РАСПОЗНАВАНИЕ НАМЕРЕНИЙ:

**Если клиент пишет:**
- "хочу купить", "купить", "заказать", "оформить заказ", "приобрести"
  → ПЕРЕХОДИ К ОФОРМЛЕНИЮ: спроси модель/характеристики, затем контакт (телефон/email)

- "узнать", "сколько стоит", "информация", "характеристики", "расскажи"
  → ДАЙ ИНФОРМАЦИЮ: цены, описания, детали

- "есть ли", "в наличии"
  → ОТВЕТЬ о наличии и цене

### 4. АЛГОРИТМ ДЕЙСТВИЙ:

**ШАГ 1: Определи намерение**
- Хочу купить → оформляй заказ
- Хочу узнать → давай информацию
- Есть вопросы → отвечай

**ШАГ 2: Действуй**

Если "ХОЧУ КУПИТЬ":
1. Уточни конкретную модель/вариант (если не указано)
2. Подтверди цену
3. Попроси контакт (телефон или email) для оформления
4. НЕ задавай лишних вопросов

Если "ХОЧУ УЗНАТЬ":
1. Дай полную информацию
2. Укажи цены
3. Дай характеристики
4. НЕ предлагай помощь в конце

## ПРИМЕРЫ ПРАВИЛЬНЫХ ОТВЕТОВ:

❌ НЕПРАВИЛЬНО: "Здравствуйте! У нас есть Nike Air Force. Если нужна информация, пожалуйста, сообщите."
✅ ПРАВИЛЬНО: "Nike Air Force стоят 6000-15000 рублей. Доступны размеры 38-45. Для оформления заказа оставьте ваш телефон."

❌ НЕПРАВИЛЬНО: "Здравствуйте! Конечно помогу. Уточните, пожалуйста, какую модель хотите?"
✅ ПРАВИЛЬНО: "Nike Air Force в наличии. Цена: 6000-15000 рублей. Оставьте ваш телефон для оформления."

❌ НЕПРАВИЛЬНО: "Здравствуйте! iPhone 15 Pro стоит 100000-120000 рублей. Если нужна информация, сообщите."
✅ ПРАВИЛЬНО: "iPhone 15 Pro: 100000-120000 рублей. Процессор A17 Pro, 128GB-1TB. Доступен в 4 цветах."

## ТВОИ ЗАДАЧИ:
1. Распознавать намерение клиента
2. Давать конкретику
3. НЕ здороваться
4. НЕ использовать эмодзи
5. Оформлять заказы когда просят купить

## ЧТО СОБРАТЬ У КЛИЕНТА:
{', '.join(self.niche.fields_to_collect)}

Собирай ненавязчиво, в процессе диалога.

{self.niche.manager_instructions or ''}
"""
        return prompt

    def _format_catalog(self) -> str:
        """Форматирует каталог в текст (универсально)"""
        if not self.catalog:
            return ""

        lines = []
        for category, items in self.catalog.items():
            lines.append(f"{category}:")
            if isinstance(items, dict):
                for item, price in items.items():
                    lines.append(f"  • {item}: {price}₽")
            elif isinstance(items, list):
                for item in items:
                    lines.append(f"  • {item}")
            lines.append("")

        return "\n".join(lines)

    def build_lead_extraction_prompt(self, messages_history: str) -> str:
        """Строит промпт для извлечения данных лида"""
        fields = ', '.join(self.niche.fields_to_collect)

        return f"""Проанализируй диалог и извлеки информацию о клиенте.

ИСТОРИЯ ДИАЛОГА:
{messages_history}

Нужно найти следующие данные: {fields}

Верни ответ СТРОГО в формате JSON:
{{
    "field_name": "значение" или null если не найдено
}}

Если какое-то поле не упомянуто — ставь null.
Не выдумывай данные, которых нет в диалоге."""
